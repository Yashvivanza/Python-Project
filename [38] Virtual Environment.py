# A Virtual environment is a tool used to indicate specific Python environments on a single machine , allowing us to work on multiple projects with different dependencies and packages without conflicts.

# This can be especially useful when working on projects that have conflicting package versions or package that are not compatible with each other.

'''

    Create a virtual environment:-
    python -m venv myenv

    Activate the virtual environment (Windows):-
    myenv\Scripts\activate.bat
'''

'''
    Deactivate the virtual environment:-
    deactivate
'''
